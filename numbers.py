
#integer
x=5
y=2825
z=-2584
print(type(x))
print(type(y))
print(type(z))
#float
x=0.255
y=11.235
z=-20.25
print(type(x))
print(type(y))
print(type(z))
#float with e
x=258e1
y=54E2
z=-0.2e0102
print(type(x))
print(type(y))
print(type(z))
#complex j as the imaginary
x=3+5j
y=98j
z=-21j
print(type(x))
print(type(y))
print(type(z))
#type conversion
x=52
y=14.23
#convert from int to float
a=float(x)
#convert from float to int
b=int(y)
#convert from int to complex
c=complex(y)
print(a)
print(b)
print(c)
print(type(a))
print(type(b))
print(type(c))
#type casting
m="s2"
n=str(5)
o=str(2.2)
print(m)
print(n)
print(o)
print(type(m))
print(type(n))
print(type(o))



